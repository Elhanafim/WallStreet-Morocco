export function getEventsByMonth(year: number, month: number): CalendarEvent[] {
  return calendarEvents.filter((e) => {
    const date = new Date(e.date);
    return date.getFullYear() === year && date.getMonth() + 1 === month;
  });
}

export function getEventsByCountry(country: string): CalendarEvent[] {
  if (country === 'Tous') return calendarEvents;
  if (country === 'Maroc') return calendarEvents.filter((e) => e.country === 'Maroc');
  return calendarEvents.filter((e) => e.country !== 'Maroc');
}

export function getUpcomingEvents(count: number = 5): CalendarEvent[] {
  const today = new Date();
  return calendarEvents
    .filter((e) => new Date(e.date) >= today)
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime())
    .slice(0, count);
}

export function getHighImpactEvents(): CalendarEvent[] {
  return calendarEvents.filter((e) => e.impactScore >= 4);
}
